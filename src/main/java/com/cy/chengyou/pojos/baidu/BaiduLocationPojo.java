package com.cy.chengyou.pojos.baidu;

public class BaiduLocationPojo {

    // 经度值
    private Double lng;
    // 纬度值
    private Double lat;

    public Double getLng() {
        return lng;
    }

    public void setLng(Double lng) {
        this.lng = lng;
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }
}
